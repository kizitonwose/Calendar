package fr.trecobat.calendar.sample.view

import android.graphics.Color
import android.graphics.drawable.Drawable
import android.os.Bundle
import android.util.TypedValue
import android.view.Menu
import android.view.MenuInflater
import android.view.View
import android.widget.TextView
import androidx.appcompat.app.AppCompatActivity
import androidx.appcompat.widget.Toolbar
import androidx.core.graphics.BlendModeColorFilterCompat
import androidx.core.graphics.BlendModeCompat
import androidx.core.view.children
import com.google.android.material.snackbar.Snackbar
import fr.trecobat.calendar.core.CalendarDay
import fr.trecobat.calendar.core.CalendarMonth
import fr.trecobat.calendar.core.DayPosition
import fr.trecobat.calendar.core.daysOfWeek
import fr.trecobat.calendar.sample.R
import fr.trecobat.calendar.sample.databinding.Example4CalendarDayBinding
import fr.trecobat.calendar.sample.databinding.Example4CalendarHeaderBinding
import fr.trecobat.calendar.sample.databinding.Example4FragmentBinding
import fr.trecobat.calendar.sample.shared.ContinuousSelectionHelper.getSelection
import fr.trecobat.calendar.sample.shared.ContinuousSelectionHelper.isInDateBetweenSelection
import fr.trecobat.calendar.sample.shared.ContinuousSelectionHelper.isOutDateBetweenSelection
import fr.trecobat.calendar.sample.shared.DateSelection
import fr.trecobat.calendar.sample.shared.dateRangeDisplayText
import fr.trecobat.calendar.sample.shared.displayText
